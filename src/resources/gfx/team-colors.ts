/**
 * S4 has two team color palette ranges per 256-color palette window:
 *
 * 1. Hardcoded gradient range (193-223): 31 entries replaced with hardcoded RGB565 team gradients.
 *    Maps pixel values 193-223 → TEAM_COLOR_PALETTES[team][1..31] (entry 0 is unused).
 *
 * 2. File-based range (224-255): 32 entries replaced with per-palette-window team colors
 *    stored in the PA6 file at paletteOffset + 256 + 32*team.
 */
