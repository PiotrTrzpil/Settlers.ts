/**
 * Convert an RGB565 value to RGBA components (0-255).
 * Uses bit replication for accurate 5/6-bit to 8-bit expansion.
 */
export function rgb565ToRgba(rgb565: number): [r: number, g: number, b: number, a: number] {
    const value1 = rgb565 & 0xff;
    const value2 = (rgb565 >> 8) & 0xff;

    let r = value2 & 0xf8;
    let g = ((value1 >> 3) | (value2 << 5)) & 0xfc;
    let b = (value1 << 3) & 0xf8;

    r |= r >> 5;
    g |= g >> 6;
    b |= b >> 5;

    return [r, g, b, 255];
}
